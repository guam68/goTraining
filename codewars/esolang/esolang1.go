package main

import "fmt"

func calc(code string) {

	var position byte
	data := make([]byte, 0)

	for i := 0; i < len(code); i++ {

		if string(code[i]) == "+" {
			position++
		} else if string(code[i]) == "." {
			data = append(data, position)
		} else {
			continue
		}
	}

	str := string(data)
	fmt.Println(str)
}

